const num = 44;
console.log(typeof num);

// EPSILON - represent the difference between 1 and the smallest floating point number greater than 1.

console.log(Number.EPSILON);

// MAX_SAFE_INTEGER - returns the maximum safe integer value

console.log(Number.MAX_SAFE_INTEGER);

// MAX_VALUE

console.log(Number.MAX_VALUE);

// MIN_VALUE - smallest possible value a number in JavaScript can have

console.log(Number.MIN_VALUE);

// MIN_SAFE_INTEGER

console.log(Number.MIN_SAFE_INTEGER);

// Number.Nan returns NaN

console.log(Number.NaN);

// NEGATIVE_INFINITY - A value that is less than MIN_VALUE

console.log(Number.NEGATIVE_INFINITY)

// POSITIVE_INFINITY

console.log(Number.POSITIVE_INFINITY)